/*

Area and Perimeter of a Rectangle
The program should:

– Prompt the user to enter the length and width of the rectangle.
– Calculate the area and perimeter of the rectangle using the length and width entered by the user.

*/

let length = Number(window.prompt("Please enter length of the rectangle"))
let width = Number(window.prompt("Please enter width of the rectangle"))

let area = length * width
let perimeter = (length + width) * 2

console.log(area) // prints -> 100
console.log(perimeter) // prints -> 40

